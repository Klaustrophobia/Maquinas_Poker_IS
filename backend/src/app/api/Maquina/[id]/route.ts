import { NextRequest, NextResponse } from "next/server";
import { MaquinaController } from "@/controllers/Maquina.controller";

const maquinaController = new MaquinaController();

export async function GET(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return await maquinaController.getMaquinaById(request, id);
}

export async function PUT(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return await maquinaController.updateMaquina(request, id);
}

export async function DELETE(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return await maquinaController.deleteMaquina(request, id);
}